// Función para obtener todos los productos en un array plano
export const getAllProducts = () => {
  return Object.values(products).flat();
};

// Función para obtener productos por categoría
export const getProductsByCategory = (categoryId) => {
  return products[categoryId] || [];
};

// Función para buscar productos
export const searchProducts = (query, categoryId = null) => {
  let productsToSearch = categoryId && categoryId !== 'todo' 
    ? getProductsByCategory(categoryId) 
    : getAllProducts();

  if (!query.trim()) return productsToSearch;

  return productsToSearch.filter(product => 
    product.name.toLowerCase().includes(query.toLowerCase()) ||
    product.brand.toLowerCase().includes(query.toLowerCase())
  );
};